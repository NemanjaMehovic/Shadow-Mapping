Cube::Cube()
{   
    
    std::vector<float> vertices(std::begin(data), std::end(data));
    int normalsStart = vertices.size();
    std::vector<float>* normals = Utils::Normals(vertices);
    vertices.insert(vertices.end(), normals->begin(), normals->end());
    delete normals;

    float* tmpData = vertices.data();

    glGenVertexArrays(1, &VAO);
    glBindVertexArray(VAO);

    glGenBuffers(1, &VBO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(float) * vertices.size(), tmpData, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);

    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)(normalsStart * sizeof(float)));
    glEnableVertexAttribArray(1);

    glBindVertexArray(0);
}

Cube::~Cube()
{
    glDeleteVertexArrays(1, &VAO);
    glDeleteBuffers(1, &VBO);
}

void Cube::activate() const
{
    glBindVertexArray(VAO);
}